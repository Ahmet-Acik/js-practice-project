# String
name = 'Ahmet'
print(name)  # Output: Ahmet
print(type(name))  # Output: <class 'str'>

# Integer
age = 25
print(age)  # Output: 25
print(type(age))  # Output: <class 'int'>

# Float
height = 5.9
print(height)  # Output: 5.9
print(type(height))  # Output: <class 'float'>

# Boolean
is_student = True
print(is_student)  # Output: True
print(type(is_student))  # Output: <class 'bool'>

# NoneType
address = None
print(address)  # Output: None
print(type(address))  # Output: <class 'NoneType'>

# List
fruits = ['apple', 'banana', 'cherry']
print(fruits)  # Output: ['apple', 'banana', 'cherry']
print(type(fruits))  # Output: <class 'list'>

# Tuple
coordinates = (10.0, 20.0)
print(coordinates)  # Output: (10.0, 20.0)
print(type(coordinates))  # Output: <class 'tuple'>

# Dictionary
person = {'name': 'Ahmet', 'age': 25}
print(person)  # Output: {'name': 'Ahmet', 'age': 25}
print(type(person))  # Output: <class 'dict'>
print(person['name'])  # Output: Ahmet
print(person.get('age'))  # Output: 25


# Set
unique_numbers = {1, 2, 3, 4, 5}
print(unique_numbers)  # Output: {1, 2, 3, 4, 5}
print(type(unique_numbers))  # Output: <class 'set'>

# Complex
complex_number = 3 + 4j
print(complex_number)  # Output: (3+4j)
print(type(complex_number))  # Output: <class 'complex'>